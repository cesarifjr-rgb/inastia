// ---- i18n Engine ----
const rawLang = localStorage.getItem('inastia-lang');
let currentLang = (rawLang === 'en') ? 'en' : 'fr'; // Whitelist valid languages

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('inastia-lang', lang);
    document.documentElement.lang = lang;

    // Update all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const text = translations[lang]?.[key];
        if (text !== undefined) {
            // Only use innerHTML if text contains HTML tags; use textContent otherwise
            if (/<[a-z][\s\S]*>/i.test(text)) {
                el.innerHTML = text;
            } else {
                el.textContent = text;
            }
        }
    });

    // Update all elements with data-i18n-placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        const text = translations[lang]?.[key];
        if (text !== undefined) {
            el.placeholder = text;
        }
    });

    // Update the flag button
    const flagBtn = document.getElementById('langToggle');
    if (flagBtn) {
        const flagGB = flagBtn.querySelector('.flag-gb');
        const flagFR = flagBtn.querySelector('.flag-fr');
        if (flagGB && flagFR) {
            flagGB.style.display = lang === 'fr' ? 'block' : 'none';
            flagFR.style.display = lang === 'fr' ? 'none' : 'block';
        }
        flagBtn.setAttribute('aria-label', lang === 'fr' ? 'Switch to English' : 'Passer en français');
        flagBtn.setAttribute('title', lang === 'fr' ? 'English version' : 'Version française');
    }

    // Re-run hero animation if heroTitle exists and has lines
    const heroTitle = document.getElementById('heroTitle');
    if (heroTitle && window.animateHeroTitle) {
        const line1 = translations[lang]?.['hero.title.line1'] || '';
        const line2 = translations[lang]?.['hero.title.line2'] || '';
        heroTitle.innerHTML = `<span class="hero-line">${line1}</span><span class="hero-line">${line2}</span>`;
        window.animateHeroTitle();
    }
}

function toggleLanguage() {
    setLanguage(currentLang === 'fr' ? 'en' : 'fr');
}

// Export for use
export { setLanguage, toggleLanguage, currentLang };
